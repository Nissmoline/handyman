  `Στην περιοχή ${name}, ${region}, η Handyman24 οργανώνει ηλεκτρολογική εξυπηρέτηση για κατοικίες, πολυκατοικίες, καταστήματα και επαγγελματικούς χώρους. Ένας ηλεκτρολόγος ${name} μπορεί να αναλάβει έλεγχο πίνακα, πρίζες, φωτισμό, βραχυκυκλώματα, σύνδεση συσκευών και ηλεκτρολογικές εργασίες με σαφή ενημέρωση πριν ξεκινήσει η τεχνική παρέμβαση.`

export const electricianAreaServices = commonServices

export const electricianAreas = areaSeeds.map((area) => {
  const title = `Ηλεκτρολόγος ${area.name}`
  const copy = area.copy || localCopyByName.get(area.name) || makeFallbackCopy(area)
  const metaTitle = makeMetaTitle(area)
  const metaDescription = makeMetaDescription(area)

  return {
    ...area,
    path: `/ilektrologos-${area.slug}`,
    title,
    headline: area.name === 'Αθήνα' ? 'Ηλεκτρολόγος Αθήνα 24 ώρες' : `${title} άμεσα`,
    metaTitle,
    metaDescription,
    copy,
    cardText: copy,
    services: commonServices,
    searchTerms: [
      `ηλεκτρολόγος ${area.name}`,
      `ηλεκτρολογος ${area.name.toLocaleLowerCase('el-GR')}`,
      `ηλεκτρολόγοι ${area.name}`,
      `ηλεκτρολόγος 24 ώρες ${area.name}`,
    ],
  }
})

export const primaryElectricianArea = electricianAreas.find((area) => area.slug === 'athina')

export const getElectricianAreaBySlug = (slug) =>
  electricianAreas.find((area) => area.slug === slug) || primaryElectricianArea

export const getRelatedElectricianAreas = (area, limit = 8) => {
  const sameRegion = electricianAreas.filter((item) => item.slug !== area.slug && item.region === area.region)
  const others = electricianAreas.filter((item) => item.slug !== area.slug && item.region !== area.region)
  return [...sameRegion, ...others].slice(0, limit)
}

